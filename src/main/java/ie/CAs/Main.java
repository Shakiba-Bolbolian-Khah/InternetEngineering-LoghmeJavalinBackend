package ie.CAs;
import java.io.BufferedReader;
import java.io.InputStreamReader;

public class Main {
    public static void main(String[] args) throws java.io.IOException {
        BufferedReader buff = new BufferedReader(new InputStreamReader(System.in));
        String line;
        while (true){
            line = buff.readLine();
            if (line == null){
                break;
            }
            
        }
    }
}
